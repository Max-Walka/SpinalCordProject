-- =============================================================================
-- Row Level Security (prototype: permissive policies)
-- The Next.js app uses the Supabase anon key without Supabase Auth JWT today,
-- so policies are granted to both `anon` and `authenticated`.
-- Tighten these before production / real patient data.
-- =============================================================================

DO $$
DECLARE
  t text;
  tables text[] := ARRAY[
    'Patient',
    'Patient Contact',
    'Patient Name',
    'Patient Address',
    'GP Enrollment',
    'Patient NHI Identifier',
    'Staff',
    'Staff Name',
    'Staff Credentials',
    'Assessment',
    'Draft Assessment',
    'Assessment Version',
    'Final Assessment',
    'Exam',
    'Exam Side',
    'Motor Score',
    'Light Touch Score',
    'Pin Prick Score',
    'Classification Result',
    'Assessment Totals'
  ];
BEGIN
  FOREACH t IN ARRAY tables
  LOOP
    EXECUTE format('ALTER TABLE %I ENABLE ROW LEVEL SECURITY', t);

    EXECUTE format(
      'CREATE POLICY %I ON %I FOR SELECT TO anon, authenticated USING (true)',
      t || ' shared select',
      t
    );
    EXECUTE format(
      'CREATE POLICY %I ON %I FOR INSERT TO anon, authenticated WITH CHECK (true)',
      t || ' shared insert',
      t
    );
    EXECUTE format(
      'CREATE POLICY %I ON %I FOR UPDATE TO anon, authenticated USING (true) WITH CHECK (true)',
      t || ' shared update',
      t
    );
    EXECUTE format(
      'CREATE POLICY %I ON %I FOR DELETE TO anon, authenticated USING (true)',
      t || ' shared delete',
      t
    );
  END LOOP;
END $$;

-- API roles (Supabase includes these by default; safe to re-run)
GRANT USAGE ON SCHEMA public TO anon, authenticated, service_role;
GRANT ALL ON ALL TABLES IN SCHEMA public TO anon, authenticated, service_role;
GRANT ALL ON ALL SEQUENCES IN SCHEMA public TO anon, authenticated, service_role;
