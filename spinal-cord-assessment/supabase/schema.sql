-- =============================================================================
-- Assessments (assessment_id = LLNN, e.g. AB12)
-- =============================================================================

CREATE TABLE "Assessment" (
  assessment_id char(4) NOT NULL PRIMARY KEY,
  "PATIENTpatient_id" bigint NOT NULL REFERENCES "Patient"(patient_id),
  "STAFFstaff_id" bigint NOT NULL REFERENCES "Staff"(staff_id),
  assessment_date date,
  status character varying,
  current_version bigint,
  injury_date date,
  created_at timestamp without time zone,
  updated_at timestamp without time zone,
  review_date date,
  CONSTRAINT assessment_id_llnn_format CHECK (assessment_id ~ '^[A-Z]{2}[0-9]{2}$')
);

