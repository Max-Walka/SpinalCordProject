-- ISNCSCI / Spinal Cord Assessment — full database schema
-- Target: PostgreSQL 15+ (Supabase)
--
-- Usage (Supabase SQL editor or psql):
--   1. Run this file on an empty `public` schema to create all tables.
--   2. If upgrading an existing DB that still has numeric assessment_id,
--      use supabase/migrations/20260521234028_assessment_id_llnn_format.sql instead.
--   3. Optionally run seed.example.sql for dev data.

-- =============================================================================
-- Core: Patient
-- =============================================================================

CREATE TABLE "Patient" (
  patient_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nhi_number character varying NOT NULL,
  date_of_birth date,
  gender character varying,
  nz_citizenship_status character varying,
  place_of_birth character varying,
  date_of_death date,
  created_at date,
  is_active character varying,
  fhir_patient_id character varying,
  ethnicity text
);

CREATE TABLE "Patient Contact" (
  contact_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "PATIENTpatient_id" bigint NOT NULL REFERENCES "Patient"(patient_id),
  email_address character varying,
  home_phone_no bigint,
  mobile_phone_co bigint,
  created_at date,
  updated_at date
);

CREATE TABLE "Patient Name" (
  name_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "PATIENTpatient_id" bigint NOT NULL REFERENCES "Patient"(patient_id),
  family_name character varying,
  given_name character varying,
  preferred_name character varying,
  prefix character varying,
  suffix character varying,
  created_at date,
  updated_at date
);

CREATE TABLE "Patient Address" (
  address_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "PATIENTpatient_id" bigint NOT NULL REFERENCES "Patient"(patient_id),
  type character varying,
  line1 character varying,
  line2 character varying,
  city character varying,
  suburb character varying,
  postal_code bigint,
  country character varying,
  created_at date,
  updated_at date
);

CREATE TABLE "GP Enrollment" (
  enrollment_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "PATIENTpatient_id" bigint NOT NULL REFERENCES "Patient"(patient_id),
  enrollment_reference character varying,
  hpi_practitioner_id character varying,
  hpi_organisation_id character varying,
  hpi_facility_id character varying,
  enrollment_start date,
  enrollment_end date,
  created_at date,
  updated_at date
);

CREATE TABLE "Patient NHI Identifier" (
  nhi_identifier_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "PATIENTpatient_id" bigint NOT NULL REFERENCES "Patient"(patient_id),
  nhi_number character varying NOT NULL UNIQUE,
  nhi_use character varying,
  assigned_at date,
  linked_at date,
  created_at timestamp without time zone
);

